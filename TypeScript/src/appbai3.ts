function cong(number1: number , number2: number): number {
    return number1 + number2;
}

function tru(number1: number , number2: number): number {
    return number1 - number2;
}

function nhan(number1: number , number2: number): number {
    return number1 * number2;
}

function chia(number1: number , number2: number): number {
    return number1 / number2;
}
